package ProgramacaoOrientadaaObjeto;

public class TreinoFelipe {
		public static void main(String[] args) {
			
			int numero = 200;
			String nome= " Felipe";
			
			System.out.print(" Treino muito Basico\n");
			
			// Comandos de impressões
			// Impressaão com quebra de linha \n (escape)
			
			System.out.println(" Treino bem Basico"); 
			
			// Impressão com quebra de linha
			
			System.out.printf(" %s - %d", " Treino",2022);
			
			// Impressão de formato
			// String (%s) uma mistura com Int (%d) 
			
			System.out.printf(" \n Valor de numero: %d%n Nome:%s ",numero,nome);
			
			// Impressao do valor de int ( entrando com int )
			// Impressão de nome " Felipe " ( entrando com String )
		}
		
		
		
}
